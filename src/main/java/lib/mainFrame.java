    private void btnFind1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFind1ActionPerformed
        // TODO add your handling code here:
        String strName = JOptionPane.showInputDialog(this,"Nhap ten nhan vien can tim (co the tim gan dung)?");
        String[] columns ={"Mã NV","Họ Tên","Tuổi","Email","Chức Vụ","Lương"};
        DefaultTableModel model = new DefaultTableModel(columns,0);
        for(Employee p:dsEmployee){
            if(strName.equals(p.getName())){
                Object[] row ={p.getID(),p.getName(),p.getAge(),p.getEmail(),p.getPosition(),p.getSalary()};
                model.addRow(row);
                continue;
            }
            if(p.getName().contains(strName)){
                Object[] row ={p.getID(),p.getName(),p.getAge(),p.getEmail(),p.getPosition(),p.getSalary()};
                model.addRow(row);
            }
        }
        tbEmployee.setModel(model);
    }//GEN-LAST:event_btnFind1ActionPerformed
