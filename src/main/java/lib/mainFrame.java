    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String id =(String) txtID.getText();
        String name =(String) txtName.getText();
        int age = Integer.parseInt(txtAge.getText());
        String email =(String) txtEmail.getText();
        String position =(String) txtPosition.getSelectedItem();
        double salary = Double.parseDouble(txtSalary.getText());
        Employee m = new Employee(id,name,age,email,position,salary);
        d.addEmployee(m);
        clearForm();
        fillTable();
    }//GEN-LAST:event_btnSaveActionPerformed
    
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        //DefaultTableModel tableModel = (DefaultTableModel) tbEmployee.getModel();
        indexrow = tbEmployee.getSelectedRow();
        if(indexrow==-1){
            JOptionPane.showMessageDialog(this,"Vui lòng chọn nhân viên cần XÓA");
            return;
        }
        String ID =(String)tbEmployee.getValueAt(indexrow, 0);
        for(Employee p: dsEmployee){
            if(ID.equals(p.getID())){
                dsEmployee.remove(p);
                d.writeListEmployees(dsEmployee);
                break;
            }
        }
        fillTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

